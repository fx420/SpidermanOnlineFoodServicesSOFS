    private void ModifyButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyButActionPerformed
        // TODO add your handling code here:
        String sourceCusID = oldCusIDTxt.getText();
        String CusID = newCusIDTxt.getText();
        String CusUN = newCusUNTxt.getText();
        String CusFN = newCusFNTxt.getText();
        String CusEmail = newCusEmailTxt.getText();
        String CusPhone = newCusPhoneTxt.getText();
        String CusAddress = newCusAddressTxt.getText();
        String CusPass = newCusPassTxt.getText();
        
        if (sourceCusID.isEmpty() || CusID.isEmpty() || CusFN.isEmpty() || CusFN.isEmpty() || CusEmail.isEmpty() || CusPhone.isEmpty() || CusAddress.isEmpty() || CusPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fields cannot be left empty");
            return;
        }

        if (!newCusPassTxt.getText().chars().allMatch(Character::isLetterOrDigit)) {
            JOptionPane.showMessageDialog(this, "Please enter valid password");
            return;
        }

        customer ModifyCustomer = new customer(CusID, CusUN, CusFN, CusEmail, CusPhone, CusAddress, CusPass);

        boolean isUpdated = customerservice.update(sourceCusID, ModifyCustomer);
        
        if (!isUpdated) {
            JOptionPane.showConfirmDialog(this, "No customer found to update");
            return;
        }

        // Reset the modify fields
        oldCusIDTxt.setText("");
        newCusIDTxt.setText("");
        newCusUNTxt.setText("");
        newCusFNTxt.setText("");
        newCusEmailTxt.setText("");
        newCusPhoneTxt.setText("");
        newCusAddressTxt.setText("");
        newCusPassTxt.setText("");

        // Show confirmation pop up
        JOptionPane.showMessageDialog(this, "Customer infomation has been Updated");

        // Update display information
        performFileRelatedTask();

    }//GEN-LAST:event_ModifyButActionPerformed


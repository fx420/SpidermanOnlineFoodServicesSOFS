    private void performFileRelatedTask() {
        StringBuilder stringBuilder = new StringBuilder();
        
        customerservice.getAll().forEach((customer) -> {
            stringBuilder.append(customer.getCusID())
                    .append("\t")
                    .append(customer.getCusUN())
                    .append("\t")
                    .append(customer.getCusFN())
                    .append("\t")
                    .append(customer.getCusEmail())
                    .append("\t")
                    .append(customer.getCusPhone())
                    .append("\t")
                    .append(customer.getCusAddress())
                    .append("\t")
                    .append(customer.getCusPass())
                    .append("\n");
        });
        ModifyCusTxt.setText(stringBuilder.toString());
    }

